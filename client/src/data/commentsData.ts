// Helper function to get total comment count for a post (including replies)
export const getTotalCommentCount = (postId: string): number => {
  const comments = postComments[postId] || [];
  return comments.reduce((total, comment) => {
    const replyCount = comment.replies?.reduce((count, reply) => {
      return count + 1 + (reply.replies?.length || 0);
    }, 0) || 0;
    return total + 1 + replyCount;
  }, 0);
};

// Helper function to get comments for a post
export const getCommentsForPost = (postId: string): Comment[] => {
  return postComments[postId] || [];
};